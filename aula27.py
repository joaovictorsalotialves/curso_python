"""
Fatiamento de strings
Fatiamento [i:f:p] [::]
obs.: a função len retorna a qtd de caracteres da str
"""

variavel = 'Olá mundo'
print(variavel[5])
print(variavel[-1])
print(variavel[3:6])
print(variavel[:6])
print(variavel[3:])
print(variavel[-8:-2])
print(variavel[:])
print(variavel[0:9:3])
print(variavel[::-1])

print(len(variavel))
